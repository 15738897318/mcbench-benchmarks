% WinAudio is a small Simulink library for accessing PC
% audio devices (eg, SoundBlaster) under Simulink as a
% real-time source.
%
%
% Real Time Simulink Blocks
%   WaveIn         - Audio input  block
%   WaveOut        - Audio output block
%   To   Wave File - Audio out to  .wav file
%   From Wave File - Audio in from .wav file
%
% Demonstrations
%   AudioScope   - Real-time audio waveform viewer
%   RecordAudio  - Records audio to disk
%   PlayFile     - Plays audio from disk