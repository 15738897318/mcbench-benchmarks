   function [y1,y2,y3] = x6fun(x1,x2,x3,x4,r1,r4,r5)
                 y1 = 4*x1^2-8*x1*x3-8*x2*x4+4*x3^2+4*x2^2+4*x4^2;
                 y21 = 8*x1*x3*x2-4*x4*r4^2+4*x4*r1^2-8*x4*x1^2-4*x4*x3^2;
                 y22 = 4*x4*r5^2+8*x1*x4*x3-4*x2*r1^2-4*x2*x3^2;
                 y23 = 4*x2*x4^2-4*x4^3-4*x2*r5^2+4*x2*r4^2;
                 y2 = y21+y22+y23;
                 y31 = 4*x1*x3*r5^2-4*x1*x3*r1^2-2*x3^2*r4^2+4*x4^2*x1^2;
                 y32 = -2*r4^2*r1^2+x3^4+4*x1^2*x3^2-4*x1^2*r5^2;
                 y33 = -4*x1*x3^3-4*x1*x4^2*x3-2*x3^2*r5^2+2*x4^2*x3^2;
                 y34 = r5^4+x4^4+4*x1*x3*r4^2-2*r4^2*r5^2+2*x3^2*r1^2;
                 y35 = 2*r1^2*r5^2+2*r4^2*x4^2+r4^4-2*r1^2*x4^2-2*r5^2*x4^2+r1^4;
                 y3 = y31+y32+y33+y34+y35;