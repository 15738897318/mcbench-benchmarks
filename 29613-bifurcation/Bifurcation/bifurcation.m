% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
set(hObject,'string','Wait');

%axis([handles.parain handles.parafin -0.25 1])

%looping 

x(:,1) = 1:1:100;



%figure('Name','Bifurication Plot Window','NumberTitle','off')
    hold on

for A = handles.parain:handles.parastep:handles.parafin

    
    %initial value of x should be less then 1
    num = handles.invalue;
    for i=1:100
        num = A*num*(1-num); 
    end

    x(1,1) = num;
    for i=2:handles.nopoints
        x(i,1)= A*x(i-1)*(1-x(i-1));
    end
    plot(A,x,'Marker','.','MarkerSize',4,'MarkerFaceColor','blue');
    
    drawnow
    
    

end

hold off

set(hObject,'string','Start');
