%% Determine h1 based on § 3 of Annex 5 of ITU-R P. 1546-4
h1 = determinationOfTransmittingAntennaHeight_h1( h_eff, h_a, Distance );

%% Step 1, 2 and 3 of Annex 5 of ITU-R P. 1546-4 are based on inputs
% Step 1:  Determine the type of the propagation path as land, cold sea or
% warm sea. If the path is mixed then determine two path types which are
% regarded as first and second propagation types. If the path can be
% represented by a single type then this is regarded as the first
% propagation type and the mixed-path method given in Step 11 is not
% required.     
% Step 2:  For any given percentage of time (in the range 1% to 50% time)
% determine two nominal time percentages as follows:  
% –	wanted time percentage > 1 and < 10, the lower and higher nominal
% percentages are 1 and 10, respectively; 
% –	wanted time percentage > 10 and < 50, the lower and higher nominal
% percentages are 10 and 50, respectively. 
% If the required percentage of time is equal to 1% or 10% or 50%, this
% value should be regarded as the lower nominal percentage time and the
% interpolation process of Step 10 is not required.  
% Step 3:  For any wanted frequency (in the range 30 to 3 000 MHz)
% determine two nominal frequencies as follows: 
% –	where the wanted frequency < 600 MHz, the lower and higher nominal
% frequencies are 100 and 600 MHz, respectively; 
% –	where the wanted frequency > 600 MHz, the lower and higher nominal
% frequencies are 600 and 2 000 MHz, respectively.   
%% 
% Step 4: is implemented in function interploateFieldStrengthForDistance
% which is called with the fuction calculateFieldStrengthForAntennaHeight
% in Step 8

% Step 5:  For the first propagation type follow Steps 6 to 11.
% Step 6:  For the lower nominal percentage time follow Steps 7 to 10.
% Step 7:  For the lower nominal frequency follow Steps 8 and 9.

%Step 8: is implemented in function calculateFieldStrengthForAntennaHeight


if tPct == 1
    if land_sea == 1
        E_at600MHz = calculateFieldStrengthForAntennaHeight(600, land_sea, FieldStrength_1pctLand_600MHz, Distance, h1, possibleAntennaHeights, tPct);
        E_at2000MHz = calculateFieldStrengthForAntennaHeight(2000, land_sea, FieldStrength_1pctLand_2000MHz, Distance, h1, possibleAntennaHeights, tPct);
    else
        E_at600MHz = calculateFieldStrengthForAntennaHeight(600, land_sea, FieldStrength_1pctSea_600MHz, Distance, h1, possibleAntennaHeights, tPct);
        E_at2000MHz = calculateFieldStrengthForAntennaHeight(2000, land_sea, FieldStrength_1pctSea_2000MHz, Distance, h1, possibleAntennaHeights, tPct);
    end
end


if tPct == 50 
    if land_sea == 1
        E_at600MHz = calculateFieldStrengthForAntennaHeight(600, land_sea, FieldStrength_50pctLand_600MHz, Distance, h1, possibleAntennaHeights, tPct);
        E_at2000MHz = calculateFieldStrengthForAntennaHeight(2000, land_sea, FieldStrength_50pctLand_2000MHz, Distance, h1, possibleAntennaHeights, tPct);
    else
        E_at600MHz = calculateFieldStrengthForAntennaHeight(600, land_sea, FieldStrength_50pctSea_600MHz, Distance, h1, possibleAntennaHeights, tPct);
        E_at2000MHz = calculateFieldStrengthForAntennaHeight(2000, land_sea, FieldStrength_50pctSea_2000MHz, Distance, h1, possibleAntennaHeights, tPct);
    end
end
%% 
% Step 9:  If the required frequency does not coincide with the lower
% nominal frequency, repeat Step 8 for the higher nominal frequency and
% interpolate or extrapolate the two field strengths using the method given
% in Annex 5, § 6. If necessary limit the result to the maximum field
% strength as given in Annex 5, § 2.    

if (Frequency == 600) 
    value = E_at600MHz;
elseif (Frequency == 2000) 
    value = E_at2000MHz;
else
    value = E_at600MHz + (E_at2000MHz - E_at600MHz) * (log10((Frequency / 600)) / log10((2000 / 600))); 
end
%%
% Step 10:  If the required percentage time does not coincide with the
% lower nominal percentage time, repeat Steps 7 to 9 for the higher nominal
% percentage time and interpolate the two field strengths using the method
% given in Annex 5, § 7.  

% NOT IMPLEMENTED AS ONLY 1% OF THE TIME AND 50% OF THE TIME ARE SUPPORTED.

%%
% Step 11:  If the prediction is for a mixed path, follow the step-by-step
% procedure given in Annex 5, § 8. This requires use of Steps 6 to 10 for
% paths of each propagation type. Note that if different sections of the
% path exist classified as both cold and warm sea, all sea sections should
% be classified as warm sea.    

% NOT IMPLEMENTED AS MIXED PATHS ARE NOT SUPPORTED

%%
% Step 12:  If information on the terrain clearance angle at a
% receiving/mobile antenna adjacent to land is available, correct the field
% strength for terrain clearance angle at the receiver/mobile using the
% method given in Annex 5, § 11.

% NOT IMPLEMENTED AS TERRAIN INFORMATION AVAILABILITY IS NOT SUPPORTED

%% 
% Step 13:  Calculate the estimated field strength due to tropospheric
% scattering using the method given in Annex 5 § 13, and, if necessary,
% adjust the final predicted field strength accordingly.  

% NOT IMPLEMENTED AS TROPOSPHERIC SCATTERING IS NOT ACCOUNTED FOR

%% 
% Step 14:  Correct the field strength for receiving/mobile antenna height
% h2 using the method given in Annex 5, § 9.  

C1 = correctionForReceivingAntennaHeight(urban_suburban_rural, land_sea, Frequency, Distance, h1, h2 );

%% 
% Step 15:  If applicable, reduce the field strength by adding the
% correction for short urban/suburban paths using the method given in Annex
% 5, § 10.

% Step 16:  If the field strength at a receiving/mobile antenna adjacent to
% land exceeded at percentage locations other than 50% is required, correct
% the field strength for the required percentage of locations using the
% method given in Annex 5, § 12.   

C2 = 0;
C3 = 0;

if land_sea == 1
    if urban_suburban_rural ==1 || urban_suburban_rural ==2
        C2 = correctionForShortUrbanSuburbanPaths(urban_suburban_rural, Frequency, Distance, h1, h_a );
    end
    C3 = locationVariability(urban_suburban_rural, Frequency, lPct, mobile, indoor);
end

FieldStrengthResult = value + C1 + C2 + C3 ;
%%
% Step 17:  If necessary, limit the resulting field strength to the maximum
% given in Annex 5, § 2. If a mixed path calculation has been made for a
% percentage time less than 50% it will be necessary to calculate the
% maximum field strength by linear interpolation between the all land and
% all-sea values. This is given by:    
%		                    dB(UV/m)	(40)
% where:
%	Efs :	free-space field strength given by equation (2) in Annex 5, § 2
%	Ese :	enhancement at small time percentages for a sea path given by equation (3) in Annex 5, § 2
%	ds :	the total sea distance (km)
%	dtotal :	the total path distance (km).


efs = 106.9 - 20*log10(Distance);
ese = 2.38*(1-exp(-Distance/8.94))*log10(50/tPct);
if land_sea == 1
    if FieldStrengthResult > efs
        FieldStrengthResult = efs;
    end
else
    if FieldStrengthResult > efs + ese
        FieldStrengthResult = efs + ese;
    end
end
%%
% Step 18:  If required, convert field strength to equivalent basic
% transmission loss for the path using the method given in Annex 5, § 16.  
Loss = 139.3 - FieldStrengthResult + 20 * log10(Frequency);
%%
% For indoor scenarios subtract the median building entry loss value based
% on the values mentioned in Section 4.9 of Annex 1 of ITU-R P.1812-2.

if indoor == 1 && land_sea == 1
    Loss = Loss + 11;
end
