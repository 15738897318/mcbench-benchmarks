function handle = inventory(game, player)

%% DISPLAY CARDS
res = get(0, 'ScreenSize');
handle = ...
    figure('Name', 'Inventory', ...
           'Units', 'pixels', ...
           'MenuBar', 'none', ...
           'NumberTitle', 'off', ...
           'Position', [res(3:4)/3, 500, 200]);
   
Infantry = ...
    axes  ('Parent', handle, ...
           'Position', [0.075, 0.1, 0.25, 0.8]);

Cavallery = ...
    axes  ('Parent', handle, ...
           'Position', [0.375, 0.1, 0.25, 0.8]);

Artillery = ...
    axes  ('Parent', handle, ...
           'Position', [0.675, 0.1, 0.25, 0.8]);
       
imshow('cards/infantry.jpg', 'Parent', Infantry)
imshow('cards/cavallery.jpg', 'Parent', Cavallery)
imshow('cards/artillery.jpg', 'Parent', Artillery)

%% DISPLAY NUMBERS
ud = get(game.Players(player), 'UserData');
n  = ud(3:7);
N  = [sum(n == 1), sum(n == 2), sum(n == 3)];

for i = 1:3
    uicontrol('style', 'text', ...
              'Parent', handle, ...
              'units', 'normalized', ...
              'FontSize', 18, ...
              'FontWeight', 'bold', ...
              'Position', [0.15 + (i-1) * 0.3, 0.2, 0.1, 0.12], ...
              'BackgroundColor', [245 228 156]/255, ...
              'String', num2str(N(i)));
end







