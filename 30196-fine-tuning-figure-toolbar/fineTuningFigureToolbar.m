    function In_optimizeAxes(hObject,eventdata)
        ti = get(gca,'TightInset');
        set(gca,'Position',[ti(1) ti(2) 1-ti(3)-ti(1) 1-ti(4)-ti(2)]);
    end
    function In_moveAxes(hObject,eventdata,v_step)
        set(gca,'Units','centimeters');
        v_pos = get(gca,'Position');
        set(gca,'Position',[(v_pos(1) + v_step(1)) (v_pos(2) + v_step(3)) (v_pos(3) - v_step(1) + v_step(2)) (v_pos(4) - v_step(3) + v_step(4))]);
        set(gca,'Units','normalized');
    end
    function In_sizeFigure(hObject,eventdata,v_size)        
        set(gcf,'Units','centimeters');
        v = get(gcf,'Position');
        if (v_size(1) ~= 0)
            v(3) = v_size(1);
        end
        if (v_size(2) ~= 0)
            v(4) = v_size(2);
        end
        set(gcf,'Position',v);
        set(gcf,'Units','normalized');
        v = get(gcf,'Position');
        set(gcf,'Position',[(0.5 - v(3)/2) (0.5 - v(4)/2) v(3:4)]);
    end
